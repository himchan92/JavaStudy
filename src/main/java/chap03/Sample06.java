package chap03;

public class Sample06 {

    public static void main(String[] args) {
        int x = 1;
        float y = 2.0f; //float 형 f 붙일것

        System.out.println(x < y);
        System.out.println(x > y);
        System.out.println(x <= y);
        System.out.println(x >= y);
        System.out.println(x == y);
        System.out.println(x != y);
    }
}
